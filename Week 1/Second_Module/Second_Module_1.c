#include <stdio.h>

int main() {
    int price = 10;
    printf("price = %d\n", price);

    float marks = 33.9;
    printf("marks = %f\n", marks);

    double temparature = 10.5;
    printf("temparature = %lf\n", temparature);

    char character = 'a';
    printf("character = %c\n", character);

    return 0;
}
